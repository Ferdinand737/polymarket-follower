def username_to_dir_name(username):
    if not username:
        return None
    name = re.sub(r'[^a-zA-Z0-9-]', '-', username).lower()
    name = re.sub(r'-+', '-', name).strip('-')
    if not re.match(r'^[a-z0-9][a-z0-9-]*[a-z0-9]$|^[a-z0-9]$', name):
        return None
    return name


